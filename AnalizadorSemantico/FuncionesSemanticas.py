def Ciclo(constante):
    try:
        numeroEntero = int(constante)
        if numeroEntero == constante:
            return True
        else:
            return False
    except:
        return False

def Condicion(constante):
    try:
        numeroEntero = int(constante)
        if numeroEntero == constante:
            return True
        else:
            return False
    except:
        return False